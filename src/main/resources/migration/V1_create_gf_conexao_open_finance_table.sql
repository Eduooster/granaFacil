CREATE TABLE T_GF_CONEXAO_OPEN_FINANCE (
   ID                      NUMBER(19)       GENERATED BY DEFAULT ON NULL AS IDENTITY,
   STATUS                  VARCHAR2(50)     NOT NULL,
   DATA_CRIACAO            TIMESTAMP        NOT NULL,
   DATA_EXPIRACAO_TOKEN    TIMESTAMP        NULL,
   ULTIMO_SYNC             TIMESTAMP        NULL,
   PLUGGY_ITEM_ID          VARCHAR2(255)    NOT NULL,


   INSTITUICAO_ID          NUMBER(19)       NOT NULL,
   USUARIO_ID              NUMBER(19)       NOT NULL,

   CONSTRAINT PK_GF_CONEXAO_OPEN_FINANCE PRIMARY KEY (ID),

   CONSTRAINT FK_GF_CONEXAO_INST
       FOREIGN KEY (INSTITUICAO_ID)
           REFERENCES GF_CONTA_FINANCEIRA (ID),

   CONSTRAINT FK_GF_CONEXAO_USUARIO
       FOREIGN KEY (USUARIO_ID)
           REFERENCES GF_USUARIO (ID)
);
