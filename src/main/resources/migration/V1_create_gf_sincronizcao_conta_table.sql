CREATE TABLE SINCRONIZACAO_CONTA (
     ID                  NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
     ID_CONTA            VARCHAR2(100),
     ORIGEM              VARCHAR2(100),

     ULTIMA_DATA_SYNC    TIMESTAMP,
     ULTIMO_ID_PROCESSADO NUMBER,

     STATUS_ATUAL        VARCHAR2(50),
     MENSAGEM_ERRO       VARCHAR2(4000),

     DATA_INICIO_EXECUCAO TIMESTAMP,
     DATA_FIM_EXECUCAO    TIMESTAMP,

     PAGINA_ATUAL        NUMBER,


     ATUALIZADO_EM       TIMESTAMP
);
