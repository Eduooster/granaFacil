CREATE TABLE TRANSACAO (
       ID                 NUMBER(19) GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
       PLUGGY_ID          VARCHAR2(100) NOT NULL,
       CONTA_ID           VARCHAR2(100) NOT NULL,

       DESCRICAO          VARCHAR2(500),
       DESCRICAO_RAW      VARCHAR2(500),
       CATEGORIA          VARCHAR2(100),
       CATEGORIA_ID       VARCHAR2(100),
       TIPO               VARCHAR2(50),
       STATUS             VARCHAR2(50),
       MOEDA              VARCHAR2(10),
       VALOR              NUMBER(20,2),
       DATA               TIMESTAMP,
       SALDO              NUMBER(20,2),

       CATEGORIA_INTERNA  VARCHAR2(50),
       DATA_IMPORTACAO    TIMESTAMP DEFAULT SYSTIMESTAMP,
       DATA_ATUALIZACAO   TIMESTAMP DEFAULT SYSTIMESTAMP,

       ATIVO              NUMBER(1) DEFAULT 1,
       ORIGEM_TRANSACAO   VARCHAR2(50)
